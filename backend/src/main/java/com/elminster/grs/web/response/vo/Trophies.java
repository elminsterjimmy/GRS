package com.elminster.grs.web.response.vo;

public class Trophies {

  private int gold;
  private int silver;
  private int brozen;
  
  /**
   * @return the gold
   */
  public int getGold() {
    return gold;
  }
  /**
   * @param gold the gold to set
   */
  public void setGold(int gold) {
    this.gold = gold;
  }
  /**
   * @return the silver
   */
  public int getSilver() {
    return silver;
  }
  /**
   * @param silver the silver to set
   */
  public void setSilver(int silver) {
    this.silver = silver;
  }
  /**
   * @return the brozen
   */
  public int getBrozen() {
    return brozen;
  }
  /**
   * @param brozen the brozen to set
   */
  public void setBrozen(int brozen) {
    this.brozen = brozen;
  }
}
